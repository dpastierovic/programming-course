namespace Basics
{
    public class Almond
    {

        private int _weight;
        private int _size;

        public Almond(int weight, int size)
        {
            _weight = weight;
            _size = size;
        }

        public int getSize()
        {
            return _weight;
        }

        public int getWeight()
        {
            return _size;
        }

    }
}